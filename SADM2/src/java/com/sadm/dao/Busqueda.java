public class Busqueda {
    
    
    private String NombrePag;
    private String urlPag;
    private String contPag;

    public String getNombrePag() {
        return NombrePag;
    }

    public void setNombrePag(String NombrePag) {
        this.NombrePag = NombrePag;
    }

    public String getUrlPag() {
        return urlPag;
    }

    public void setUrlPag(String urlPag) {
        this.urlPag = urlPag;
    }

    public String getContPag() {
        return contPag;
    }

    public void setContPag(String contPag) {
        this.contPag = contPag;
    }
    
    
}
